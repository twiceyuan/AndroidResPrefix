package com.twiceyuan.script.android.res.prefix.bean

enum class ResType {
    /**
     * Drawable 资源，包含图片文件、xml 类 drawable 文件和 values 中的 <drawable> 定义
     */
    Drawable,

    /**
     * MipMap 资源，包含图片文件、xml 类 mipmap 文件
     */
    MipMap,

    /**
     * 布局文件
     */
    Layout,

    /**
     * 字符串资源
     */
    String,

    /**
     * 字符数组资源
     */
    StringArray,

    /**
     * 量词字符串资源
     */
    StringPlurals,

    /**
     * dimen 定义
     */
    Dimension,

    /**
     * Style 定义
     */
    Style,

    /**
     * Anim 资源
     */
    Animation,

    /**
     * Menu 资源
     */
    Menu,

    /**
     * 颜色资源
     */
    Color,
}